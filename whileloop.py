# Loops são sequências de código que vão
# se repetir enquanto certas condições
# forem atendidas.

eu = 1
while eu <= 10:
    print(eu)
    eu = eu + 1
print ('O loop acabou.')
# O código acima quer dizer que, enquanto eu for menor ou 
# igual a 10, terá um "print" e em seguida, soma-se 1 ao total,
# até o total de 10.